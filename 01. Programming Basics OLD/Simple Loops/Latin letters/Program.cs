using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Simple_Loops
{
    class Program
    {
        static void Main(string[] args)
        {
            for (char i = (char)'a'; i <= 'z'; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
